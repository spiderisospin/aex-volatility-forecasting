def evaluate_forecast(y_true: pd.Series, y_pred: pd.Series, y_bench: pd.Series,
                      model_name: str = "Model", bench_name: str = "Benchmark") -> dict:
    """
    Evaluate forecast performance relative to benchmark using RMSE, MAE and QLIKE.
    In our model we assume log-variance forecasts. RMSE and MAE are computed on log scale,
    while QLIKE is computed on the variance scale after exponentiating values.

    Parameters
        y_true
            Observed log-variance series.
        y_pred
            Log-variance forecast from main model.
        y_bench
            Log-variance forecast from benchmark model.
        model_name
            Name of evaluated model.
        bench_name
            Name of benchmark model.

    Returns
        metrics
            Contains evaluation metrics of our models.
    """
    #RMSE
    rmse = np.sqrt(mean_squared_error(y_true, y_pred))
    rmse_bench = np.sqrt(mean_squared_error(y_true, y_bench))
    improvement_rmse = (rmse_bench - rmse) / rmse_bench

    #MAE
    mae = np.mean(np.abs(y_true - y_pred))
    mae_bench = np.mean(np.abs(y_true - y_bench))
    improvement_mae = (mae_bench - mae) / mae_bench

    #QLIKE
    epsilon = 1e-12
    y_true_var = np.clip(np.exp(y_true), epsilon, None)
    y_pred_var = np.clip(np.exp(y_pred), epsilon, None)
    y_bench_var = np.clip(np.exp(y_bench), epsilon, None)

    ratio_pred = y_true_var / y_pred_var
    ratio_bench = y_true_var / y_bench_var

    qlike = np.mean(ratio_pred - np.log(ratio_pred) -1)
    qlike_bench = np.mean(ratio_bench - np.log(ratio_bench) - 1)

    improvement_qlike = (qlike_bench - qlike) / qlike_bench

    metrics = {
        "model_name": model_name,
        "bench_name": bench_name,
        "rmse": rmse,
        "mae": mae,
        "rmse_bench": rmse_bench,
        "mae_bench": mae_bench,
        "improvement_rmse": improvement_rmse,
        "improvement_mae": improvement_mae,
        "qlike": qlike,
        "qlike_bench": qlike_bench,
        "improvement_qlike": improvement_qlike
    }

    return metrics

def forecast_metrics(metrics: dict) -> None:
    """
    Display the forecast evaluation metrics for model and benchmark.
    This function put the RMSE, MAE and QLIKE metrics into a DataFrame. It does the
    same for the improvement metrics, and subsequently concatenates these two. Finally,
    the dataframe is displayed.

    Parameters
        metrics
            Dictionary containing metrics necessary for model

    Returns
        None

    """
    df = pd.DataFrame({
        "RMSE": [metrics["rmse_bench"], metrics["rmse"]],
        "MAE": [metrics["mae_bench"], metrics["mae"]],
        "QLIKE": [metrics["qlike_bench"], metrics["qlike"]],
    }, index=[metrics["bench_name"], metrics["model_name"]]).round(4)

    improvement = pd.DataFrame({
        "RMSE": [f"{metrics['improvement_rmse']:.2%}"],
        "MAE": [f"{metrics['improvement_mae']:.2%}"],
        "QLIKE": [f"{metrics['improvement_qlike']:.2%}"]
    }, index=["Improvement"])

    df = pd.concat([df, improvement])
    display(df)

def forecast_stability(results: dict[str, pd.Series], n_periods: int) -> pd.DataFrame:
    """
    Evaluate signal stability across out-of-sample subperiods

    Here, this function splits the out-of-sample forecast into n_periods amount of periods
    and notes the RMSE, MAE and QLIKE for our HAR benchmark and HAR+XGBoost model. Subsequently,
    a metric table is displayed along with a bar plot, comparing the model performance for each
    period.

    Parameters:
        results
            Dictionary containing model results. Required data: y_true, y_pred, y_har.
        n_periods
            Amount of out-of-sample periods used in signal stability analysis.

    Returns:
        df
            DataFrame containing forecasting metrics and improvements for each period.
    """

    y_true = results["y_true"]
    y_pred = results["y_pred"]
    y_har = results["y_har"]

    #align
    common_idx = y_true.index.intersection(y_pred.index).intersection(y_har.index)
    y_true, y_pred, y_har= y_true.loc[common_idx], y_pred.loc[common_idx], y_har.loc[common_idx]

    n = len(common_idx)
    period_size = n // n_periods

    rows = []

    #evaluate for every OOS period
    for i in range(n_periods):
        start = i * period_size
        end = (i + 1) * period_size if i < n_periods - 1 else n
        idx = common_idx[start:end]

        metrics = evaluate_forecast(y_true.loc[idx], y_pred.loc[idx], y_har.loc[idx],
                                    model_name="HAR+XGB", bench_name="HAR")

        rows.append({
            "period": i+1,
            "start": idx[0],
            "end": idx[-1],
            "n_obs": len(idx),
            "RMSE": f"HAR: {metrics['rmse_bench']:.4f} | XGB: {metrics['rmse']:.4f} | Improvement: {metrics['improvement_rmse']:.2%}",
            "MAE": f"HAR: {metrics['mae_bench']:.4f} | XGB: {metrics['mae']:.4f} | Improvement: {metrics['improvement_mae']:.2%}",
            "QLIKE": f"HAR: {metrics['qlike_bench']:.4f} | XGB: {metrics['qlike']:.4f} | Improvement: {metrics['improvement_qlike']:.2%}",
            "imp_rmse": metrics["improvement_rmse"],
            "imp_mae": metrics["improvement_mae"],
            "imp_qlike": metrics["improvement_qlike"]
        })

    df = pd.DataFrame(rows)
    display(df)

    df = df.set_index("period")[["imp_rmse", "imp_mae", "imp_qlike"]]

    df.plot(kind="bar", figsize=(6,3))
    plt.title("Forecast Stability Across OOS Periods")
    plt.ylabel("Improvement over HAR")
    plt.grid()
    plt.show()

    return df

def OOS_analysis(feature_df: pd.DataFrame, main_asset: str, selected_features: dict[str, list[str]], train_ratio: float,
                 opt_params: dict, window: int, oos_ratio: float = 0.2) -> dict[str, pd.Series]:
    """
    Evaluating the HAR-XGBoost performance on out-of-sample data.

    This function creates an out-of-sample period, with length of this period determined
    by oos_ratio. Subsequently, the HAR-XGBoost walk-forward backtest is ran on the entire
    data set. Following this, a forecast is made on the out of sample period.

    Parameters:
        feature_df
            DataFrame containing target and engineered features.
        main_asset
            Ticker of target asset.
        selected_features
            Dictionary mapping assets to lists of selected feature names.
        train_ratio
            Fraction of observations used in initial training window.
        opt_params
            Dictionary containing optimal parameters found by Optuna.

    Returns:
        results
            Dictionary containing y_true, y_pred, y_har.
    """
    n = len(feature_df)
    oos_start = int((1 - oos_ratio) * n)

    train_df = feature_df.iloc[:oos_start]
    oos_df = feature_df.iloc[oos_start:]

    print(f"Train period: {train_df.index[0]} -> {train_df.index[-1]}")
    print(f"OOS period:   {oos_df.index[0]} -> {oos_df.index[-1]}")

    #run full backtest once, then restrict evaluation to final OOS period
    res = har_xgb_backtest(feature_df, main_asset, selected_features,
                           train_ratio, opt_params, window)

    y_true, y_pred, y_har = res["y_true"], res["y_pred"], res["y_har"]

    #restrict to OOS
    oos_mask = y_true.index >= oos_df.index[0]
    y_true, y_pred, y_har = y_true[oos_mask], y_pred[oos_mask], y_har[oos_mask]

    results = {"y_true": y_true, "y_pred": y_pred, "y_har": y_har}
    return results